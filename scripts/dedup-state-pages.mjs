#!/usr/bin/env node
/**
 * dedup-state-pages.mjs
 *
 * Replace the generic "state-overview" + "eligibility" boilerplate on per-state
 * lawsuit guides with verified, state-specific content so the pages stop being
 * near-duplicates of each other.
 *
 * Usage:
 *   node scripts/dedup-state-pages.mjs <tortSlug>
 *   node scripts/dedup-state-pages.mjs roundup
 *
 * The script reads the per-tort dedup config from
 *   src/data/state-dedup/{tortSlug}.json
 * which holds a hand-curated, source-verified payload per state (see SHAPE
 * below). It then mutates the matching markdown files in src/content/state-guides/.
 *
 * Idempotent: if a state file already has the inserted section, it is skipped.
 *
 * IMPORTANT — YMYL accuracy checklist (read before editing any dedup config):
 *   1. Every verdict figure must be the FINAL post-appeal amount (not the
 *      intermediate trial-court number).
 *   2. Every claim that names a date, dollar amount, court, judge, statute,
 *      or appellate ruling must have a primary or credible-secondary source
 *      URL listed in `sources`.
 *   3. "No state action" / absence claims must be reworded as an
 *      absence-of-evidence statement tied to the specific records reviewed
 *      (state legislature bill-search + state AG news) and those records
 *      must appear in `sources`.
 *   4. The `sources` list must substantiate the *claims actually made* in the
 *      `body` HTML, not just generic background material.
 *   5. Hearing vs. trial must be stated explicitly; never describe a Daubert
 *      hearing as a trial.
 *
 * SHAPE of src/data/state-dedup/{tortSlug}.json:
 *   {
 *     "_meta": {
 *       "tortSlug": "roundup",
 *       "tortName": "Roundup Cancer",
 *       "filePrefix": "roundup",
 *       "reviewDate": "2026-06-13",
 *       "pageStructure": "legacy"   // "legacy" (default) or "newcase" (add-case-states.mjs pages)
 *     },
 *     "states": {
 *       "california": {
 *         "stateName": "California",
 *         "article": "a" | "an",    // "an" for Ohio, Illinois, Arkansas, etc.
 *         "h2": "What stands out about Roundup litigation in California?",
 *         "body": "<p>...verified state-specific HTML...</p>",
 *         "sources": [
 *           { "label": "Source name", "url": "https://..." }
 *         ],
 *         "faqs": [
 *           { "question": "...", "answer": "..." },
 *           { "question": "...", "answer": "..." }
 *         ]
 *       }
 *     }
 *   }
 */
import { readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const ROOT = process.cwd();
const STATE_GUIDES_DIR = join(ROOT, "src/content/state-guides");

// Marker = the last line of the boilerplate "state-overview" section that is
// identical across every legacy state guide. We anchor the insertion here so
// the unique section appears immediately after the boilerplate intro.
const INSERT_MARKER = [
  "<p>State law may still matter for deadlines, damages, claim evaluation, and certain procedural issues.</p>",
  "</section>"
].join("\n");

// New-case pages (generated by scripts/add-case-states.mjs) use a different
// structure — state-overview / eligibility / filing-process, with no "State law
// may still matter" line and already case-specific FAQs. For pageStructure
// "newcase" we anchor the insertion on the eligibility section opener and
// PREPEND a state-specific FAQ instead of replacing generic ones.
const NEWCASE_INSERT_MARKER = `<section id="eligibility">`;

const A = `target="_blank" rel="noopener noreferrer"`;

// We use a stable section id of "state-roundup-context" historically; keep it
// per-tort so multiple dedup runs on the same file don't collide.
function sectionId(tortSlug) {
  return `state-${tortSlug}-context`;
}

function renderSectionWithId({ tortSlug, h2, body, sources }) {
  const sourcesHtml = sources.length === 0
    ? ""
    : `\n<p class="mt-4 text-sm text-muted"><strong>Sources:</strong> ${sources
        .map((s) => `<a href="${s.url}" ${A}>${s.label}</a>`)
        .join("; ")}.</p>`;
  return `<section id="${sectionId(tortSlug)}">
<h2>${h2}</h2>
${body.trim()}${sourcesHtml}
</section>`;
}

function renderFaqYaml(faqs) {
  return faqs
    .map((f) => `  -\n    question: "${escapeYaml(f.question)}"\n    answer: "${escapeYaml(f.answer)}"`)
    .join("\n");
}

function escapeYaml(s) {
  return s.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

function findGenericFaqRange(text, { tortName, stateName, article }) {
  const lines = text.split("\n");
  const firstQuestion = `    question: "What is the ${tortName} lawsuit in ${stateName} about?"`;
  const secondQuestion = `    question: "Can ${article} ${stateName} resident join a national lawsuit?"`;
  const firstQuestionIndex = lines.indexOf(firstQuestion);
  if (firstQuestionIndex < 1 || lines[firstQuestionIndex - 1].trim() !== "-") return null;

  const firstItemStart = firstQuestionIndex - 1;
  let secondItemStart = -1;
  let thirdItemStart = -1;
  for (let i = firstQuestionIndex + 1; i < lines.length; i += 1) {
    if (lines[i].trim() !== "-") continue;
    if (secondItemStart === -1) secondItemStart = i;
    else {
      thirdItemStart = i;
      break;
    }
  }
  if (secondItemStart === -1 || thirdItemStart === -1) return null;
  if (!lines.slice(secondItemStart, thirdItemStart).includes(secondQuestion)) return null;

  return { lines, firstItemStart, thirdItemStart };
}

function applyOne({ tortSlug, tortName, filePrefix, reviewDate, stateSlug, payload, pageStructure }) {
  const filename = `${filePrefix}-${stateSlug}.md`;
  const filePath = join(STATE_GUIDES_DIR, filename);
  let text;
  let eol;
  try {
    text = readFileSync(filePath, "utf8");
    eol = text.includes("\r\n") ? "\r\n" : "\n";
    text = text.replace(/\r\n/g, "\n");
  } catch (e) {
    return { stateSlug, status: "missing-file", filePath };
  }

  if (text.includes(`id="${sectionId(tortSlug)}"`)) {
    return { stateSlug, status: "already-deduped" };
  }

  // New-case structure: insert the unique section immediately BEFORE the
  // eligibility section and PREPEND a state-specific FAQ, leaving the existing
  // case-specific FAQs in place.
  if (pageStructure === "newcase") {
    if (!payload.h2 || !payload.body || !Array.isArray(payload.sources) || payload.sources.length === 0) {
      return { stateSlug, status: "invalid-content-payload" };
    }
    if (!Array.isArray(payload.faqs) || payload.faqs.length < 1 || payload.faqs.length > 2) {
      return { stateSlug, status: "invalid-faq-payload" };
    }
    if (text.split(NEWCASE_INSERT_MARKER).length - 1 !== 1) {
      return { stateSlug, status: "eligibility-marker-not-found-or-ambiguous" };
    }
    const newSection = renderSectionWithId({
      tortSlug,
      h2: payload.h2,
      body: payload.body,
      sources: payload.sources
    });
    text = text.replace(NEWCASE_INSERT_MARKER, `${newSection}\n\n${NEWCASE_INSERT_MARKER}`);

    const ncLines = text.split("\n");
    const faqsIdx = ncLines.indexOf("faqs:");
    if (faqsIdx === -1) {
      return { stateSlug, status: "faqs-block-not-found" };
    }
    ncLines.splice(faqsIdx + 1, 0, ...renderFaqYaml(payload.faqs).split("\n"));
    text = ncLines.join("\n");

    return doneWith(filePath, text, eol, reviewDate, { stateSlug, status: "ok" });
  }

  if (text.split(INSERT_MARKER).length - 1 !== 1) {
    return { stateSlug, status: "marker-not-found-or-ambiguous" };
  }

  if (!payload.h2 || !payload.body || !Array.isArray(payload.sources) || payload.sources.length === 0) {
    return { stateSlug, status: "invalid-content-payload" };
  }
  if (!Array.isArray(payload.faqs) || payload.faqs.length !== 2) {
    return { stateSlug, status: "invalid-faq-payload" };
  }

  // Validate the boilerplate FAQ range before changing anything. This keeps
  // the operation atomic: hand-edited pages are left untouched for review.
  const faqRange = findGenericFaqRange(text, {
    tortName,
    stateName: payload.stateName,
    article: payload.article || "a"
  });
  if (!faqRange) {
    return { stateSlug, status: "generic-faqs-not-found" };
  }

  // 1) Insert the unique section immediately after the boilerplate intro.
  const newSection = renderSectionWithId({
    tortSlug,
    h2: payload.h2,
    body: payload.body,
    sources: payload.sources || []
  });
  text = text.replace(INSERT_MARKER, `${INSERT_MARKER}\n\n${newSection}`);

  // 2) Replace the two generic FAQs with state-specific ones. Recompute the
  // range after section insertion so the line indexes cannot drift.
  const updatedRange = findGenericFaqRange(text, {
    tortName,
    stateName: payload.stateName,
    article: payload.article || "a"
  });
  const updatedLines = updatedRange.lines;
  updatedLines.splice(
    updatedRange.firstItemStart,
    updatedRange.thirdItemStart - updatedRange.firstItemStart,
    ...renderFaqYaml(payload.faqs).split("\n")
  );
  text = updatedLines.join("\n");

  return doneWith(filePath, text, eol, reviewDate, { stateSlug, status: "ok" });
}

function doneWith(filePath, text, eol, reviewDate, result) {
  // Bump lastUpdated + lastReviewed to today's dedup date.
  text = text.replace(/lastUpdated: "[^"]*"/, `lastUpdated: "${reviewDate}"`);
  text = text.replace(/lastReviewed: "[^"]*"/, `lastReviewed: "${reviewDate}"`);
  writeFileSync(filePath, text.replace(/\n/g, eol));
  return result;
}

function main() {
  const tortSlug = process.argv[2];
  if (!tortSlug) {
    console.error("Usage: node scripts/dedup-state-pages.mjs <tortSlug>");
    process.exit(2);
  }
  const configPath = join(ROOT, "src/data/state-dedup", `${tortSlug}.json`);
  let config;
  try {
    config = JSON.parse(readFileSync(configPath, "utf8"));
  } catch (e) {
    console.error(`ERROR: cannot read ${configPath}: ${e.message}`);
    process.exit(2);
  }
  const tortName = config._meta?.tortName || tortSlug;
  // Most guide filenames use the URL slug. AFFF is the known exception:
  // lawsuitSlug "afff-pfas" maps to files prefixed "afff-firefighting-foam".
  const filePrefix = config._meta?.filePrefix || tortSlug;
  const reviewDate = config._meta?.reviewDate;
  if (!/^\d{4}-\d{2}-\d{2}$/.test(reviewDate || "")) {
    console.error("ERROR: config._meta.reviewDate must use YYYY-MM-DD");
    process.exit(2);
  }
  const pageStructure = config._meta?.pageStructure || "legacy";
  if (!["legacy", "newcase"].includes(pageStructure)) {
    console.error(`ERROR: config._meta.pageStructure must be "legacy" or "newcase"`);
    process.exit(2);
  }
  const states = config.states || {};
  const stateSlugs = Object.keys(states);
  if (stateSlugs.length === 0) {
    console.error("ERROR: config has no states");
    process.exit(2);
  }
  console.log(`Deduping ${stateSlugs.length} ${tortSlug} state pages…`);
  let ok = 0;
  let skipped = 0;
  let bad = 0;
  for (const stateSlug of stateSlugs) {
    const result = applyOne({
      tortSlug,
      tortName,
      filePrefix,
      reviewDate,
      stateSlug,
      payload: states[stateSlug],
      pageStructure
    });
    if (result.status === "ok") {
      ok += 1;
      console.log(`  [ok]      ${tortSlug}-${stateSlug}.md`);
    } else if (result.status === "already-deduped") {
      skipped += 1;
      console.log(`  [skip]    ${tortSlug}-${stateSlug}.md  (already deduped)`);
    } else {
      bad += 1;
      console.log(`  [WARN]    ${tortSlug}-${stateSlug}.md  ${result.status}`);
    }
  }
  console.log(`\nUpdated: ${ok}   Skipped: ${skipped}   Warnings: ${bad}`);
  if (bad > 0) process.exit(1);
}

main();
